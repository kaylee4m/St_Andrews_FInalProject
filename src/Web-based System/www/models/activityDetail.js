// This file is a database model file, used to define a collection of json data, export database operations
const mongoose = require('mongoose')
const db = require('../db/mongodb')

// The structure of the json data 
var ActivityDetailShema = new mongoose.Schema({
    activityTitle: String,
    date: String,
    nameOfPrimaryContact: String,
    contactDetailsOfPrimaryContact: String,
    undergraduates: Number,
    postgraduates: Number,
    postdoctorate: Number,
    academicStaff: Number,
    profStaff: Number,
    external: Number,
    universityUnit: String,
    universitySchool: String,
    eventTitle: String,
    project: String,
    Postcode: String,
    targetAudience: String,
    evaluated: String,
    involve: String,
    generalPublic: String,
    note: String
}, {
        timestamps: { createdAt: 'createdTime', updatedAt: 'updatedTime' }
    })

// Model of json generation
var ActivityDetail = db.model('ActivityDetail', ActivityDetailShema)

// Export json
const Dao = {
    create(payload) {
        return new ActivityDetail(payload).save()
    },
    saveMany(arr) {
        return ActivityDetail.insertMany(arr.map(v => new ActivityDetail(v)))
    },
    getCount() {
        return ActivityDetail.count({})
    },
    getList(pageSize, pageNum) {
        const start = (pageNum - 1) * pageSize
        return ActivityDetail.find({}).sort({ createdTime: 'desc' }).skip(start).limit(pageSize)
    },
    getAll() {
        return ActivityDetail.find({})
    }
}

module.exports = Dao


